// Παράδειγμα με επανάληψης for που χρησιμοποιείται για να εκτελέσει τον κώδικα εντός του βρόχου για 
// τις τιμές του i από 1 έως 5. Κάθε φορά που η επανάληψη εκτελείται, ο αριθμός i αυξάνεται κατά 1 και τυπώνεται.

#include  <stdio.h>
int main() {
    // Χρησιμοποιούμε τη δομή επανάληψης for για να εκτυπώσουμε τους αριθμούς 1 έως 5
    for (int i = 1; i <= 5; i++) {
        printf("Αριθμός: %d\n", i);
    }
    return 0;
}
