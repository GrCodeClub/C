// Όταν εκτελέσετε αυτόν τον κώδικα, θα σας ζητηθεί να εισάγετε έναν ακέραιο αριθμό. Μετά την εισαγωγή σας, το πρόγραμμα θα τυπώσει τον αριθμό που εισαγάγατε.
#include  <stdio.h>
int main() {
    int number;
    // Ζητάμε τον χρήστη να εισάγει έναν ακέραιο αριθμό
    printf("Παρακαλώ εισάγετε έναν ακέραιο αριθμό: ");
    scanf("%d", &number);
    // Εκτυπώνουμε τον αριθμό που εισήγαγε ο χρήστης
    printf("Εισαγωγή: %d\n", number);
    return 0;
}
